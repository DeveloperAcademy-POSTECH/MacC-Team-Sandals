        9. 다른 회원의 개인정보를 그 동의 없이 수집, 저장, 공개하는 행위
        10. 회사의 직원이나 콘텐츠의 관리자를 가장하거나 사칭하여 또는 타인의 명의를 도용하여 게시물 등을 작성하거나 이메일을 발송하는 행위
