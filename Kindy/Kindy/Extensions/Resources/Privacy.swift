        3. 기타 **샌들즈**가 제휴계약 등을 통해 회원에게 제공하는 일체의 콘텐츠
    2. 회사는 필요한 경우 콘텐츠의 내용을 추가 또는 변경할 수 있습니다. 단, 이 경우 회사는 추가 또는 변경 내용을 회원에게 공지해야 합니다.

