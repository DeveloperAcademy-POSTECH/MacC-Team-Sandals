        3. 타인의 명예를 훼손하거나 모욕하는 행위
        4. 타인의 지적재산권 등의 권리를 침해하는 행위
        5. 해킹행위 또는 바이러스의 유포 행위
        6. 타인의 의사에 반하여 광고성 정보 등 일정한 내용을 계속적으로 전송 또는 게시하는 행위
