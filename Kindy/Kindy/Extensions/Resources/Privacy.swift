    회사는 콘텐츠 사용자분의 편의를 위해 콘텐츠 이용과 관련된 각종 고지 및 기타 **샌들즈** 콘텐츠 홍보를 포함한 다양한 정보를 **샌들즈** 콘텐츠에 표시하거나 사용자의 휴대폰 문자, 알림 메시지(Push Notification) 등으로 발송할 수 있으며 콘텐츠 사용자분은 이에 동의합니다. 이 경우 콘텐츠 사용자분의 통신환경 또는 요금구조에 따라 콘텐츠 사용자분이 데이터 요금 등을 부담할 수 있습니다. 한편 회사는 콘텐츠 사용자분이 수집에 동의한 콘텐츠 내 활동 정보를 기초로 회사에게 직접적인 수익이 발생하지 않거나 회사가 판매하는 상품과 직접적인 관련성이 없는 한도에서 다른 콘텐츠 사용자분 등이 판매하는 상품 또는 콘텐츠에 관한 정보를 위와 같은 방법으로 콘텐츠 사용자분에게 보낼 수 있으며 콘텐츠 사용자분은 이에 동의합니다. 물론 콘텐츠 사용자분은 관련 법령상 필요한 내용을 제외하고 언제든지 이러한 정보에 대한 수신 거절을 할 수 있으며, 이 경우 회사는 즉시 위와 같은 정보를 보내는 것을 중단합니다.

